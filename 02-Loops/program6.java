public class program6 {
    public static void main(String[] args) {
        // 5️⃣ Print multiplication table of a number
        int n = 4;

        for (int i = 1; i <= 10; i++) {
            System.out.println(n + " * " + i + " = " + n * i);
        }
    }
}
